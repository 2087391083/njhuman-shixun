package com.hqyj.mymana.pojo;

public class EmployeeFiles {
    private Integer id;

    private Integer fileNum;

    private Integer employeeNum;

    private String filesName;

    private String filesSuma;

    private String filesNotes;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getFileNum() {
        return fileNum;
    }

    public void setFileNum(Integer fileNum) {
        this.fileNum = fileNum;
    }

    public Integer getEmployeeNum() {
        return employeeNum;
    }

    public void setEmployeeNum(Integer employeeNum) {
        this.employeeNum = employeeNum;
    }

    public String getFilesName() {
        return filesName;
    }

    public void setFilesName(String filesName) {
        this.filesName = filesName;
    }

    public String getFilesSuma() {
        return filesSuma;
    }

    public void setFilesSuma(String filesSuma) {
        this.filesSuma = filesSuma;
    }

    public String getFilesNotes() {
        return filesNotes;
    }

    public void setFilesNotes(String filesNotes) {
        this.filesNotes = filesNotes;
    }
}